int main() {
    // Declare variables
    int x = 5;
    int y = 10;
    
    // Perform arithmetic operations
    int sum = x + y;
    int difference = x - y;
    int product = x * y;
    int quotient = x / y;
    
    // Print results
    std::cout << "Sum: " << sum << std::endl;
    std::cout << "Difference: " << difference << std::endl;
    std::cout << "Product: " << product << std::endl;
    std::cout << "Quotient: " << quotient << std::endl;
    
    return 0;
}
